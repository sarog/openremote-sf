package org.openremote.android.console.bindings;


@SuppressWarnings("serial")
public class LayoutContainer extends BusinessEntity {

   protected int left;
   protected int top;
   protected int width;
   protected int height;
   
   public int getLeft() {
      return left;
   }

   public int getTop() {
      return top;
   }

   public int getWidth() {
      return width;
   }

   public int getHeight() {
      return height;
   }

   
}
